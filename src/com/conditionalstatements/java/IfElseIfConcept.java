package com.conditionalstatements.java;

public class IfElseIfConcept {

	public static void main(String[] args) {
		int x=3467;			//always if the first condition is true, it displays only the first statement output
							//if the first condition will be the false, it will check for the next condition in this (if else) case
		if (x<100) {
			System.out.println("first condition is true");
			
		}else if (x<1000) {
			System.out.println("second is true");
			
		} else if (x<=1000){
			System.out.println("third is true");
			
		}else {
			System.out.println("fourth is true");
			
		}
			
		
			
		}
	}

